#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (i < ac)
	{
		j = 0;
		while (av[ac - 1][j])
		{
			write(1, &av[ac - 1][j], 1);
			j++;
		}
		write(1, "\n", 1);
		ac--;
	}
	return (0);
}
