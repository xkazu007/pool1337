char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while(src[i] != '\0')
	{
		dest[i] = src[i];
	printf("i = '%d' and src[i] = '%c' \n",i,src[i]);
		
		i++;
	}
	dest[i] = '\0';
	return dest;
}
