char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 1;
	if (str[0] <= 'z' && str[0] >= 'a' )
	{
		str[0] -= 32;
	}
	while (str[i])
	{
		if (!((str[i - 1] <= 'z' && str[i - 1] >= 'a')
				|| (str[i - 1] <= 'Z' && str[i - 1] >= 'A')
				|| (str[i - 1] >= '0' && str[i - 1] <= '9')))
		{
			if (str[i] <= 'z' && str[i] >= 'a')
			{
			str[i] -= 32;
			}		
		}
		else if (str[i] <= 'Z' && str[i] >= 'A')
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}
